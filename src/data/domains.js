/** Get all domains with published papers */
export const getPublishedDomains = () => DOMAINS.filter(d => d.paper.status === PAPER_STATUS.PUBLISHED);

/** Get domains sorted by beta (descending), excluding nulls */
export const getDomainsByBeta = () =>
  DOMAINS.filter(d => d.beta !== null).sort((a, b) => b.beta - a.beta);

/** Get domains by urgency level */
export const getDomainsByUrgency = (urgency) =>
  DOMAINS.filter(d => d.policyLab?.urgency === urgency);

/** Get domains grouped by sector for ExecutiveBrief */
export const getDomainsBySectorGrouped = () => {
  const groups = {};
  DOMAINS.forEach(d => {
    if (!d.sector) return;
    if (!groups[d.sector]) groups[d.sector] = [];
    groups[d.sector].push(d);
  });
  return SECTORS.map(s => ({ sector: s, domains: groups[s] || [] })).filter(g => g.domains.length > 0);
};

